void main () {

  // A PROGRAM USING CONDITIONAL STATEMENTS TO PRINT COMMENTS BASED ON STUDENTS GRADES.

  //INITIALIZE VARIABLE MARKS AND STORE THE VALUE 80.

  int marks = 80 ;

  //USING IF ELSE STATEMENTS.

  if (marks > 85) {
    print("EXCELLENT");

  } else if (marks >= 75) {
    print("VERY GOOD");

  } else if (marks >= 65) {
    print("GOOD");

  } else {
    print("AVERAGE");
  }
      
}

//OUTPUT: VERY GOOD.